class CVideoAdapter
{
public:
    virtual bool PortWrite16(int port, int data) = 0;
    virtual bool PortWrite8(int port, int data) = 0;
    virtual bool PortRead8(int port, uint8_t& data) { assert(0); return false; }
    virtual bool Interrupt(CicoContext::cicocontext_t* ctx) = 0;
    virtual void Write(uint32_t dwAddr, uint8_t bWrite) = 0;
    virtual uint8_t Read(uint32_t dwAddr) = 0;
    virtual uint32_t GetPixel(int x, int y) = 0;
    virtual void SetPixel(int x, int y, int c) = 0;
};
class CText : public CVideoAdapter
{
    uint8_t screen[25*80*2];
    uint8_t font[256*14];
    uint8_t font1[256*14];
    uint32_t palette[16] = {
        0x000000, 0x0000b0, 0x00b000, 0x00b0b0, 0xb00000, 0xb000b0, 0xb0b000, 0xb0b0b0,
        0x808080, 0x0000ff, 0x00ff00, 0x00ffff, 0xff0000, 0xff00ff, 0xffff00, 0xffffff};

public:
    CText() : CVideoAdapter()
    {
        memcpy(font, egafont14x8, sizeof(font));
    }
    virtual bool PortWrite16(int port, int data) { return false; }
    virtual bool PortWrite8(int port, int data) { 
        static int lastPort = 0, lastData = 0;
        if ( port == 0x3c0 )
        {
            static int index = -1;
            if (index == -1)
            {
                if (data <= 0x10)
                    index = data;
            }
            else
            {
                SetPaletteIndex(index, data);
                index = -1;
            }
            return true;
        }
        return false; 
    }
    virtual bool PortRead8(int port, uint8_t& data) { assert(0); return false; }
    virtual bool Interrupt(CicoContext::cicocontext_t* ctx) {
        if (ctx->a.r16 == 0x0103)
        {
//     set cursor type
            return true;
        }
        if (ctx->a.r16 == 0x1003)
        {
//     set cursor type
            return true;
        }
        if (ctx->a.r16 == 0x1110)
        {     
            /*
             AX    1110H
             BH    height of each character (bytes per character definition)
             BL    font block to load (EGA: 0-3; VGA: 0-7)
             CX    number of characters to redefine
             DX    ASCII code of the first character defined at ES:BP
             ES:BP address of font-definition information
             */

//            memcpy(font, &ctx->memory8(ctx->_es, ctx->_bp), ctx->c.r16*14);
            //font = &ctx->memory8(ctx->_es, ctx->_bp);
            memcpy(font1, &ctx->memory8(ctx->_es, ctx->_bp), ctx->c.r16*14);
            return true;
        }
        if (ctx->a.r16 == 0x1201)
            return true;
        if (ctx->a.r16 == 0x1103)
            return true;
        assert(0);
return false;
    }
    virtual void Write(uint32_t dwAddr, uint8_t bWrite)
    {
        int ofs = dwAddr - 0xb8000;
        assert(ofs >= 0 && ofs < sizeof(screen));
        screen[ofs] = bWrite;
    }
    virtual uint8_t Read(uint32_t dwAddr) { return 0; }
    virtual uint32_t GetPixel(int x, int y){
        if (!font)
            return 0;
        int row = y/14;
        int col = x/8;
        int subx = x%8;
        int suby = y%14;
        uint8_t symbol = screen[(row*80+col)*2];
        uint8_t attr = screen[(row*80+col)*2+1];
        if ((attr & 0xf) > 7)
            return (font1[symbol*14+suby] & (128>>subx)) ? palette[attr & 0xf] : palette[attr >> 4];
        else
            return (font[symbol*14+suby] & (128>>subx)) ? palette[attr & 0xf] : palette[attr >> 4];
//        return 0;
    };
    virtual void SetPixel(int x, int y, int c) { return; };
private:
    void SetPaletteIndex(int index, int rgb)
    {
        int r = ((rgb & 4) ? 2 : 0) + ((rgb & 32) ? 1 : 0);
        int g = ((rgb & 2) ? 2 : 0) + ((rgb & 16) ? 1 : 0);
        int b = ((rgb & 1) ? 2 : 0) + ((rgb & 8) ? 1 : 0);
        r = r * 255 / 3;
        g = g * 255 / 3;
        b = b * 255 / 3;
        palette[index] = b | (g << 8) | (r << 16);
    }
};

class CCga : public CVideoAdapter
{
    enum {
        MemSize = 0x10000*2
    };

public:
    uint8_t memory[MemSize];

    uint32_t _cgaPalette[4];
    uint32_t _cgaBackground;

    CCga()
    {
        _cgaPalette[0] = 0x000000;
        _cgaPalette[1] = 0x808080;
        _cgaPalette[2] = 0xb0b0b0;
        _cgaPalette[3] = 0xffffff;
        
        _cgaPalette[0] = 0x000000;
        _cgaPalette[1] = 0x55ffff;
        _cgaPalette[2] = 0xbb0000;
        _cgaPalette[3] = 0xffffff;

    }
    virtual void SetPixel(int x, int y, int c) override
    {
        bool _xor = c & 0x80;
        c &= 0x7f;
        
        if ((y&1) == 0)
        {
            y /= 2;
            uint8_t& pix4 = memory[y*80+x/4];
            int bit = 6-((x&3)*2);
            int mask = 3 << bit;
            if (_xor)
                pix4 = pix4 ^ (c << bit);
            else
                pix4 = (pix4 & ~mask) | (c << bit);
        } else
        {
            y /= 2;
            uint8_t& pix4 = memory[0x2000+y*80+x/4];
            int bit = 6-((x&3)*2);
            int mask = 3 << bit;
            if (_xor)
                pix4 = pix4 ^ (c << bit);
            else
                pix4 = (pix4 & ~mask) | (c << bit);
        }
        static int q=0;
        if (q++ > 100)
        {
            q = 0;
            _sync();
        }
    }

    virtual bool PortWrite8(int port, int data) override
    {
        return false;
    }

    virtual bool PortWrite16(int port, int data) override
    {
        return false;
    }

    virtual bool Interrupt(CicoContext::cicocontext_t* ctx) override
    {
        //if ( CTextMode::Interrupt( ah, al, bh, bl ) )
//            return true;
        if (ctx->a.r8.h == 0x02)
            return true;

        if ( ctx->a.r8.h == 0x0b )
        {
            if ( ctx->b.r8.h == 0x00 )
            {
                _cgaBackground = ctx->b.r8.l;
                return true;
            }
            if ( ctx->b.r8.h == 0x01 )
            {
                if (ctx->b.r8.l == 0x00)
                {
                    _cgaPalette[0] = 0x000000;
                    _cgaPalette[1] = 0x55aa55;
                    _cgaPalette[2] = 0xaa5555;
                    _cgaPalette[3] = 0xaaaa55;
                    return true;
                }
                if (ctx->b.r8.l == 0x01)
                {
                    _cgaPalette[0] = 0x000000;
                    _cgaPalette[1] = 0x55ffff;
                    _cgaPalette[2] = 0xff55ff;
                    _cgaPalette[3] = 0xffffff;
                    return true;
                }
            }
        }
        if (ctx->a.r8.h == 0x0c)
        {
            SetPixel(ctx->c.r16, ctx->c.r16, ctx->a.r8.l);
            return true;
        }

        return false;
    }

    virtual void Write(uint32_t dwAddr, uint8_t bWrite) override
    {
        assert(dwAddr >= 0xb800*16);
        dwAddr -= 0xb800 * 16;
        assert(dwAddr < MemSize);
        assert(dwAddr < 17000);
//        dwAddr %= 16000;
        memory[dwAddr] = bWrite;
        
        static int t=0;
        if (t++ > 8000){
            t = 0; _sync();
        }
    }

    virtual uint8_t Read(uint32_t dwAddr) override
    {
        assert(dwAddr >= 0xb800*16);
        dwAddr -= 0xb800 * 16;
        assert(dwAddr < MemSize);
        return memory[dwAddr];
    }


    uint32_t GetPixel(int x, int y) override
    {
        if ((y&1) == 0)
        {
            y /= 2;
            uint8_t pix4 = memory[y*80+x/4];
            pix4 <<= (x&3)*2;
            return _cgaPalette[pix4 >> 6];
        } else
        {
            y /= 2;
            uint8_t pix4 = memory[0x2000+y*80+x/4];
            pix4 <<= (x&3)*2;
            return _cgaPalette[pix4 >> 6];
        }
        return 0;
    }
};

