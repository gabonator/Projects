var EGA = {
  palette : [0x000000, 0x0000b4, 0x00a800, 0x00a8a8, 0xb80000, 0xb800b8, 0xc07830, 0x989898, 
             0xb85804, 0x2c2cfc, 0x00fc00, 0x28f0f0, 0xfc3408, 0x646464, 0xf0e81c, 0xfcfcfc],
  memory : null,
  access : false,
  interrupt : (h, l) => {
    if (h == 0x10 && l == 0x03)
      return true;
    if (h == 0x00 && l == 0x03)
      return true;
    if (h == 0x00 && l == 0x04)
      return true;
    if (h == 0x0f)
    {
      r16[ax] = 0x5003;
      r8[bh] = 0x00;
      return true;
    }
    if (h == 0x11 && l == 0x12)
      return true;
    if (h == 0x11 && l == 0x30)
    {
      r16[cx] = 0x10;
      r8[dl] = 0x18;
      es = 0xc000;
      bp = 0x500;
      return true;
    }
    if (h == 0x08)
    {
      r16[ax] = 0x720;
      return true;
    }
    if (h == 0x06)
      return true;
    if (h == 0x02) // gotoxy
      return true;
    if (h == 0x12 && l == 0x00 && r8[bl] == 0x10)
    {
        r16[bx] = 0x0003;
        r16[cx] = 0x0009;
        return true;
    }
    if (h == 0x1a)
    {
      r16[ax] = 0x5003;
      r8[bh] = 0x00;
      return true;
    }
    if (h == 0x10 && l == 0x10)
      return true;
    console.log("ah = "+h.toString(16) + ", al="+l.toString(16))
  },
  getPixel : function(x, y)
  {
        var row = Math.floor(y/3);
        var col = Math.floor(x/8);
        var subx = x%8;
        var suby = y%14;
        if (suby > 13)
            suby = 13;
        var ofs = 0x8000; 
        var symbol = this.memory[ofs+(row*80+col)*2];
        var attr = this.memory[ofs+(row*80+col)*2+1];
        return (egafont14x8[symbol*14+suby] & (128>>subx)) ? this.palette[attr & 0xf] : this.palette[attr >> 4];
  },
  write : function(addr, val)
  {
        this.memory[addr - 0xb0000] = val;
        this.access = true;
  },
  read : function(addr)
  {
        return this.memory[addr - 0xb0000];
  },
  init : function()
  {
    this.memory = new Uint8Array(0x10000*8);
  },
    PortWrite16: function(port, val)
    {
	if ( port == 0x3c4 )
	{
	return true;	
	}	
	if ( port == 0x3d4 )
	{
		if ( (val & 0x00ff) == 0x0c )
		{
			return true;
		}
		if ( (val & 0x00ff) == 0x0d )
		{
			return true;
		}
	}
	if ( port == 0x3ce )
	{
		return;
	}
        console.log("unhandled write16 ", port.toString(16));

	assert(0);
        return false;

    },
    PortWrite8: function(port, val)
    {
        if (port == 0x3d4)
        {
            return true;
        }
        if (port == 0x3d5)
        {
            return true;
        }
        if ( port == 0x3ce )
        {
            return true;
        }
        if (port == 0x3cf)
        {
            return true;
        }
        if (port == 0x3c8)
        {
            return true;
        }
        if (port == 0x3c9)
        {
            return true;
        }
        if (port == 0x3c4)
        {
            return true;
        }
        if (port == 0x3c5)
        {
            return true;
        }

        console.log("unhandled write8 ", port.toString(16));
	assert(0);

        return false;
    },
    PortRead8: function(port)
    {
      if (EGA._crtReg == 0x0c)
          return EGA.cfgAddr>>8;
      assert(0);
    }
};

